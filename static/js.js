const soni = document.getElementById('id_soni');
const soni_id = document.getElementById('id_soni_id');
soni.addEventListener('input', function() {
  soni_id.textContent = soni.value;
  if(soni){
    soni_id.style.backgroundColor = "#f1c40f";
  } else {
    soni_id.style.backgroundColor = "#ffffff";
  }
});

