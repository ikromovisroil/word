const son = document.getElementById('id_son');
const son_id = document.getElementById('id_son_id');
son.addEventListener('input', function() {
  son_id.textContent = son.value;
  if(son){
    son_id.style.backgroundColor = "#f1c40f";
  } else {
    son_id.style.backgroundColor = "#ffffff";
  }
});

